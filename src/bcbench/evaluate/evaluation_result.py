from dataclasses import dataclass
from typing import Optional
from enum import Enum
from pathlib import Path
import json
from bcbench.core.logger import get_logger

logger = get_logger(__name__)


class FailureReason(Enum):
    TIMEOUT = "timeout"
    MAX_STEPS = "max_steps"
    COMPILATION_ERROR = "compilation_error"
    TEST_FAILURE = "test_failure"
    AGENT_ERROR = "agent_error"
    INVALID_PATCH = "invalid_patch"


@dataclass(slots=True)
class EvaluationResult:
    """Result of evaluating an agent on a dataset entry."""

    instance_id: str
    version: str
    resolved: bool = False

    # Agent execution details
    agent_patch: Optional[str] = None  # The patch generated by the agent
    step_count: int = 0  # Number of steps the agent took

    # Error tracking
    error_message: Optional[str] = None
    failure_reason: Optional[FailureReason] = None

    # Metadata
    evaluation_time_seconds: Optional[float] = None

    def save(self, output_dir: Path, result_file: str = "instance_results.jsonl") -> None:
        """Save this evaluation result to a JSONL file in the specified directory."""

        output_file = output_dir / result_file
        with open(output_file, "a") as f:
            result_dict = {
                "instance_id": self.instance_id,
                "resolved": self.resolved,
                "agent_patch": self.agent_patch,
                "step_count": self.step_count,
                "error_message": self.error_message,
                "environment_setup_version": self.version,
                "evaluation_time_seconds": self.evaluation_time_seconds,
            }
            f.write(json.dumps(result_dict) + "\n")

        logger.info(f"Saved evaluation result for {self.instance_id} to {output_file}")
